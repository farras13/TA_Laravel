			          	<span class="subheading">Kebun Anggrek Singosari</span>
			            <h2 class="mb-4" style="font-size: 34px; text-transform: capitalize;">Sejarah Kebun</h2>
			            <p>PT Sari Bumi Mulya adalah perusahaan swasta nasional yang bergerak dalam produksi berbagai bibit dan tanaman anggrek unggul dengan memanfaatkan berbagai strategi pertanian khususnya strategi Plasma Tani. Bagi perusahaan,  Plasma Tani adalah program pemberdayaan pertanian anggrek yang membantu perekonomian petani plasma dimana para petani plasma tidak harus membeli bibit untuk melakukan kegiatan pertanian, melainkan para petani telah mendapatkan bibit dari perusahaan dan akan dikembangkan oleh petani plasma.</p>
			            <p>Budidaya anggrek oleh PT Sari Bumi Mulya telah dilakukan semenjak tahun 2010 yang didirikan oleh Bapak Asy’ari, S.E. dan terus berkembang hingga saat ini. PT Sari Bumi Mulya awalnya dibentuk untuk menyalurkan hobi dan kesukaan terhadap tanaman anggrek oleh Pak Asy’ari. Karena basic yang dimiliki oleh Pak Asy’ari sebelumnya adalah pebisnis, maka akhirnya menemukan potensi bisnis dalam proses budidaya tanaman anggrek yang sebelumnya masih menjadi hobi.</p>
			            <p>Pada 2011, budidaya anggrek yang awalnya sebagai hobi perlahan lahan berubah menjadi peluang usaha dengan skala yang masih kecil. Hingga akhirnya pada tahun 2012, dibentuklah sebuah kebun anggrek yang berlokasi di Kecamatan Singosari Kota Malang dikarenakan banyaknya koleksi yang dibeli pada 2010 yang ternyata diminati oleh pasar kolektor pada waktu itu sampai terdengar oleh salah satu kolektor terkenal di Jakarta yang mengharuskan Pak Asy’ari membentuk suatu kebun anggrek.</p>
                        <p>Pada tahun 2014, PT Sari Bumi Mulya mulai melakukan persilangan tanaman anggrek dalam kebun sendiri, dimana sebelumnya tiap bibit diimpor dari Thailand. Hal tersebut berdampak pada pasar tanaman anggrek yang akhirnya mulai berkembang hingga melakukan ekspansi dengan ekspor ke beberapa negara di Asia Tenggara.</p>
                        <p>Hingga saat ini, PT Sari Bumi Mulya terus berkembang dikarenakan banyak nya inovasi yang telah dilakukan. Untuk kedepannya, Pak Asy’ari berharap agar PT Sari Bumi Mulya terus berkembang agar menjadi Perusahaan Anggrek yang menghasilkan inovasi perkebunan anggrek yang mendukung pertanian maju, mandiri, dan modern.</p>
